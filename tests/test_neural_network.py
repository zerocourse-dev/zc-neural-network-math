class TestIntegration:
    def test_xor_like_classification(self):
        """Network should learn a nonlinear 2D classification pattern."""
        rng = np.random.default_rng(42)
        # Create XOR-like data
        x = np.array([[0, 0], [0, 1], [1, 0], [1, 1]] * 25, dtype=float)
        labels = np.array([0, 1, 1, 0] * 25)
        x += rng.normal(0, 0.1, x.shape)  # Add noise
        y = one_hot_encode(labels, 2)

        net = NeuralNetwork([2, 16, 2], activations=["relu", "softmax"], seed=42)
        opt = SGD(learning_rate=0.5)
        trainer = Trainer(net, opt, cross_entropy_loss, cross_entropy_loss_derivative)
        result = trainer.train(x, y, epochs=100, batch_size=20, seed=42)
        assert result["loss_history"][-1] < result["loss_history"][0]

    def test_loss_decreases_during_training(self):
        x, labels = generate_spiral_data(n_points_per_class=100, n_classes=3, seed=0)
        y = one_hot_encode(labels, 3)
        net = NeuralNetwork([2, 64, 32, 3], seed=0)
        opt = SGD(learning_rate=0.1)
        trainer = Trainer(net, opt, cross_entropy_loss, cross_entropy_loss_derivative)
        result = trainer.train(x, y, epochs=50, batch_size=32, seed=0)
        # Loss should decrease significantly
        assert result["loss_history"][-1] < result["loss_history"][0] * 0.5

    def test_spiral_classification_above_chance(self):
        """Trained network should beat random chance on spiral data."""
        x, labels = generate_spiral_data(n_points_per_class=100, n_classes=3, seed=7)
        y = one_hot_encode(labels, 3)
        net = NeuralNetwork([2, 64, 32, 3], seed=7)
        opt = SGD(learning_rate=0.1)
        trainer = Trainer(net, opt, cross_entropy_loss, cross_entropy_loss_derivative)
        trainer.train(x, y, epochs=100, batch_size=32, seed=7)
        result = trainer.evaluate(x, y)
        assert result["accuracy"] > 0.4  # Chance is ~0.33

    def test_momentum_converges_faster(self):
        """MomentumSGD should converge faster than vanilla SGD."""
        x, labels = generate_spiral_data(n_points_per_class=80, n_classes=3, seed=42)
        y = one_hot_encode(labels, 3)

        # Vanilla SGD
        net1 = NeuralNetwork([2, 32, 3], seed=42)
        opt1 = SGD(learning_rate=0.05)
        trainer1 = Trainer(net1, opt1, cross_entropy_loss, cross_entropy_loss_derivative)
        result1 = trainer1.train(x, y, epochs=30, batch_size=32, seed=42)

        # Momentum SGD
        net2 = NeuralNetwork([2, 32, 3], seed=42)
        opt2 = MomentumSGD(learning_rate=0.05, momentum=0.9)
        trainer2 = Trainer(net2, opt2, cross_entropy_loss, cross_entropy_loss_derivative)
        result2 = trainer2.train(x, y, epochs=30, batch_size=32, seed=42)

        # Momentum should achieve lower loss
        assert result2["loss_history"][-1] < result1["loss_history"][-1]
