class TestRelu:
    def test_positive_values_unchanged(self):
        x = np.array([1.0, 2.0, 3.0])
        result = relu(x)
        np.testing.assert_array_equal(result, np.array([1.0, 2.0, 3.0]))

    def test_negative_values_become_zero(self):
        x = np.array([-1.0, -2.0, -3.0])
        result = relu(x)
        np.testing.assert_array_equal(result, np.array([0.0, 0.0, 0.0]))

    def test_mixed_values(self):
        x = np.array([-2.0, -1.0, 0.0, 1.0, 2.0])
        result = relu(x)
        np.testing.assert_array_equal(result, np.array([0.0, 0.0, 0.0, 1.0, 2.0]))

    def test_preserves_shape(self):
        x = np.array([[1.0, -1.0], [-2.0, 3.0]])
        result = relu(x)
        assert result.shape == (2, 2)


