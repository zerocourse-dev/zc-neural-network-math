class TestSGD:
    def test_weights_change_after_update(self):
        layer = Layer(4, 3, seed=0)
        x = np.random.default_rng(1).standard_normal((2, 4))
        layer.forward(x)
        layer.backward(np.ones((2, 3)))
        weights_before = layer.weights.copy()
        optimizer = SGD(learning_rate=0.1)
        optimizer.update([layer])
        assert not np.array_equal(layer.weights, weights_before)

    def test_learning_rate_affects_step_size(self):
        layer1 = Layer(4, 3, seed=0)
        layer2 = Layer(4, 3, seed=0)
        x = np.random.default_rng(1).standard_normal((2, 4))
        layer1.forward(x)
        layer1.backward(np.ones((2, 3)))
        layer2.forward(x)
        layer2.backward(np.ones((2, 3)))
        w_before = layer1.weights.copy()
        SGD(learning_rate=0.1).update([layer1])
        SGD(learning_rate=0.01).update([layer2])
        diff1 = np.abs(layer1.weights - w_before).sum()
        diff2 = np.abs(layer2.weights - w_before).sum()
        assert diff1 > diff2


class TestMomentumSGD:
    def test_weights_change_after_update(self):
        layer = Layer(4, 3, seed=0)
        x = np.random.default_rng(1).standard_normal((2, 4))
        layer.forward(x)
        layer.backward(np.ones((2, 3)))
        weights_before = layer.weights.copy()
        optimizer = MomentumSGD(learning_rate=0.1, momentum=0.9)
        optimizer.update([layer])
        assert not np.array_equal(layer.weights, weights_before)

    def test_momentum_builds_velocity(self):
        """After multiple steps with same gradient, momentum should make larger updates."""
        layer = Layer(4, 3, seed=0)
        optimizer = MomentumSGD(learning_rate=0.01, momentum=0.9)
        x = np.random.default_rng(1).standard_normal((2, 4))

        # First step
        layer.forward(x)
        layer.backward(np.ones((2, 3)))
        w_before_1 = layer.weights.copy()
        optimizer.update([layer])
        step1 = np.abs(layer.weights - w_before_1).sum()

        # Second step with same gradient direction
        layer.forward(x)
        layer.backward(np.ones((2, 3)))
        w_before_2 = layer.weights.copy()
        optimizer.update([layer])
        step2 = np.abs(layer.weights - w_before_2).sum()

        assert step2 > step1  # Momentum should increase step size


